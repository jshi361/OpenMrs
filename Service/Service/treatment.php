	<h1>Treatment</h1>

	<p>Treatment consists of fluids and activity modifications<br/>
	The flu is treated primarily with rest and fluid to let the body fight the infection on its own.<br/>
	Over-the-counter anti-inflammatory pain relievers may help with symptoms.<br/>
