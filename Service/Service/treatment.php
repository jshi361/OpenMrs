	Supportive care: Fluid replacement <br />
	Self-care: Bed rest and Throat lozenge <br />
	Medications: Decongestant, Cough medicine, Nonsteroidal anti-Inflammatory drug, Analgesic, and Antiviral drug<br/>
	Specialists: Pediatrician and Primary care provider (PCP)<br/>

